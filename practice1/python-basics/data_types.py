x = 5 # int
y = 2.8   # float
z = "Hello"  # str
a = True      # bool
b = ["apple", "banana"]  # list
print(type(x))
print(type(y))
print(type(z))
print(type(a))
print(type(b))

# Sequence types
list_example = ["apple", "banana", "cherry"]
tuple_example = ("apple", "banana", "cherry")
range_example = range(6)
print(type(list_example))
print(type(tuple_example))
print(type(range_example))