# Example 1: Single line comment
# This is a comment
print("Hello, World!")

# Example 2: Comment after code
print("Comments are useful!")  # This prints a message

# Example 3: Multi-line comments using multiple #
# This is a comment
# written in
# more than just one line
print("Multi-line comment example")

# Example 4: Multi-line string as comment (not recommended but works)
"""
This is a multi-line string
that can be used as a comment.
Python will ignore string literals that are not assigned to a variable.
"""
print("Using multi-line strings")

# Example 5: Commenting out code for debugging
x = 5
# x = 10  # This line is commented out
print("x =", x)  # Will print 5, not 10