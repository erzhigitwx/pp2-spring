# Example 1: Basic if-else
a = 200
b = 33
if b > a:
    print("b is greater than a")
else:
    print("b is not greater than a")

# Example 2: Even or odd
num = 7
if num % 2 == 0:
    print("Even number")
else:
    print("Odd number")

# Example 3: Age check
age = 16
if age >= 18:
    print("Adult")
else:
    print("Minor")

# Example 4: Password check
password = "secret"
if password == "secret123":
    print("Access granted")
else:
    print("Access denied")

# Example 5: Temperature
temp = 25
if temp > 30:
    print("Hot weather")
else:
    print("Comfortable weather")