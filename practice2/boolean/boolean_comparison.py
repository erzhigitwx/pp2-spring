# Example 1: Comparison operators
x = 10
y = 5
print(x > y)
print(x < y)
print(x == y)
print(x != y)

# Example 2: Greater than or equal, less than or equal
a = 10
b = 10
print(a >= b)
print(a <= b)

# Example 3: Comparing strings
print("apple" > "banana")
print("apple" == "apple")

# Example 4: Using comparison in variables
result = 5 > 3
print(result)
print(type(result))

# Example 5: Chain comparisons
x = 5
print(1 < x < 10)
print(10 < x < 20)