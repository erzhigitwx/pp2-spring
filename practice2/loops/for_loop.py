# Example 1: Iterate through list
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# Example 2: Loop through string
for char in "Python":
    print(char)

# Example 3: Using range()
for i in range(6):
    print(i)

# Example 4: Range with start and end
for i in range(2, 6):
    print(i)

# Example 5: Range with step
for i in range(0, 10, 2):
    print(i)